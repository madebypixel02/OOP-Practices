package java.edu.uoc.prac2;

/**
 * Enumeration to handle the direction a map element is moving
 */
public enum Direction {
    /**
     * Direction going up
     */
    NORTH,
    /**
     * Direction going down
     */
    SOUTH,
    /**
     * Direction going right
     */
    EAST,
    /**
     * Direction going left
     */
    WEST
}
